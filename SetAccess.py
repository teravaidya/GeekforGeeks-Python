# Python program to demonstrate
# Accessing of elements in a set

# Creating a set
set1 = set(["Geeks", "For", "Geeks"])
print("\n Initial Set")
print(set1)

# Accessing element using
# for loop.
print("Elements of set:")
for i in set1:
  print(i, end=" ")

# Checking the element
# using in keyword
print("Geeks" in set1)

