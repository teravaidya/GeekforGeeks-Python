# Python Program for
# Creation of String

# Creating a String
# with single Quotes
String1 = 'Welcome to Geeks world'
print("String with the use of single quotes:")
print(String1)
print()

# Creating a String
# with double Quotes
String1 = "I'm a Geek"
print("String with the use of double quotes:")
print(String1)
print()

# Creating a String
# with triple Quotes
String1 = '''I'm a Geek and I live in the world of Geeks'''
print("String with the use of triple quotes:")
print(String1)
print()

# Creating String with triple
# Quotes allows multiple lines
String1 = '''Geeks
              for
              Life'''
print("String with the use of multiple lines:")
print(String1)
print()


